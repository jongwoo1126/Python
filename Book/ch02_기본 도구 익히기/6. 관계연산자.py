# 동등비교
num1 = 100
num2 = 20

bool_result = num1 == num2    # 두 변수의 값이 같은지 비교
print(bool_result)

bool_result = num1 != num2    # 두 변수의 값이 다른지 비교
print(bool_result)

# 크기비교
bool_result = num1 > num2
print(bool_result)

bool_result = num1 >= num2
print(bool_result)

bool_result = num1 < num2
print(bool_result)

bool_result = num1 <= num2
print(bool_result)